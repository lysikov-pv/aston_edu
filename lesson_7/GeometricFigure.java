package lesson_7;

public interface GeometricFigure {

    /**
     * Выводит в консоль информацию о фигуре
     */
    void info();

    /**
     * @return Возвращает периметр фигуры
     */
    double getPerimeter();
    /**
     * @return Возвращает площадь фигуры
     */
    double getArea();

}
